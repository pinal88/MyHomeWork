package myhomeworkpackage;

public class IfElse4 {
    public static void main(String[] args) {
        int even = 180;
        if (even%180==0) {
            System.out.println("180 is even number");
        } else {
            System.out.println("180 is odd number");
        }
        int odd = 20;
        if (odd%20==0) {
            System.out.print("20 is even number");
        } else {
            System.out.print("20 is odd number");
    }
}}
